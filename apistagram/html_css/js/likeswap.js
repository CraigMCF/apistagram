// Swapping the heart button under the single image

		function changeImage() {
    if(document.getElementById('myImage').src == 'http://www.craigmorrison.ca/tatagram/images/heart-icon-off.jpg') {
        document.getElementById('myImage').src = 'http://www.craigmorrison.ca/tatagram/images/heart-icon-on.jpg';
    } else if(document.getElementById('myImage').src == 'http://www.craigmorrison.ca/tatagram/images/heart-icon-on.jpg') {
        document.getElementById('myImage').src = 'http://www.craigmorrison.ca/tatagram/images/heart-icon-off.jpg';
    }
    

}

